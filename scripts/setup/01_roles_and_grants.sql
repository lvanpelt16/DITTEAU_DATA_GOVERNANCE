GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE IR_ANALYST_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE IR_ANALYST_ROLE;

