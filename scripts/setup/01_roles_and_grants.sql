-- ============================================================
-- DITTEAU DATA GOVERNANCE - ROLES AND GRANTS
-- ============================================================
-- Purpose: Create role hierarchy for data access control
-- Run As: ACCOUNTADMIN (or SECURITYADMIN)
-- Run Order: 2 (After 00_governance_schema.sql)
-- ============================================================

USE ROLE ACCOUNTADMIN;

-- ============================================================
-- SECTION 1: CREATE DATA ACCESS ROLES
-- ============================================================

-- ───────────────────────────────────────────────────────────
-- Engineering and Development Roles
-- ───────────────────────────────────────────────────────────

CREATE ROLE IF NOT EXISTS DATA_ENGINEER_ROLE
    COMMENT = 'Data engineers - can read/write all layers, see partial PII for debugging';

CREATE ROLE IF NOT EXISTS DBT_CLOUD_PROD_ROLE
    COMMENT = 'dbt Cloud production service account - full transformation access';

CREATE ROLE IF NOT EXISTS DBT_CLOUD_DEV_ROLE
    COMMENT = 'dbt Cloud development - read all, write to DEV schemas only';

-- ───────────────────────────────────────────────────────────
-- Analyst Roles (by Department)
-- ───────────────────────────────────────────────────────────

CREATE ROLE IF NOT EXISTS DATA_ANALYST_ROLE
    COMMENT = 'General data analysts - read access to DISTRIBUTE layer only';

CREATE ROLE IF NOT EXISTS IR_ANALYST_ROLE
    COMMENT = 'Institutional Research analysts - aggregate data access';

CREATE ROLE IF NOT EXISTS REGISTRAR_ANALYST_ROLE
    COMMENT = 'Registrar analysts - full academic data access';

CREATE ROLE IF NOT EXISTS ENROLLMENT_ANALYST_ROLE
    COMMENT = 'Enrollment/Admissions analysts - prospective student data';

CREATE ROLE IF NOT EXISTS FINANCIAL_AID_ANALYST_ROLE
    COMMENT = 'Financial Aid analysts - financial and eligibility data';

-- ───────────────────────────────────────────────────────────
-- Administrative Roles
-- ───────────────────────────────────────────────────────────

CREATE ROLE IF NOT EXISTS DITTEAU_DATA_ADMIN
    COMMENT = 'Ditteau Data platform administrators - full access to all';

-- ============================================================
-- SECTION 2: ROLE HIERARCHY
-- ============================================================

-- Set up role inheritance (roles granted to other roles)

-- Admin roles inherit all analyst roles
GRANT ROLE DATA_ANALYST_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE IR_ANALYST_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE REGISTRAR_ANALYST_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE ENROLLMENT_ANALYST_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE FINANCIAL_AID_ANALYST_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE DATA_ENGINEER_ROLE TO ROLE DITTEAU_DATA_ADMIN;
GRANT ROLE GOVERNANCE_ADMIN_ROLE TO ROLE DITTEAU_DATA_ADMIN;

-- Data engineers can use analyst roles for testing
GRANT ROLE DATA_ANALYST_ROLE TO ROLE DATA_ENGINEER_ROLE;

-- Grant to SYSADMIN for administrative purposes
GRANT ROLE DITTEAU_DATA_ADMIN TO ROLE SYSADMIN;

-- ============================================================
-- SECTION 3: WAREHOUSE GRANTS
-- ============================================================

-- Create warehouses if they don't exist
CREATE WAREHOUSE IF NOT EXISTS TRANSFORM_WH
    WAREHOUSE_SIZE = 'SMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE
    COMMENT = 'Primary warehouse for dbt transformations';

CREATE WAREHOUSE IF NOT EXISTS ANALYST_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE
    COMMENT = 'Warehouse for analyst queries';

-- Grant warehouse access
GRANT USAGE ON WAREHOUSE TRANSFORM_WH TO ROLE DATA_ENGINEER_ROLE;
GRANT USAGE ON WAREHOUSE TRANSFORM_WH TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT USAGE ON WAREHOUSE TRANSFORM_WH TO ROLE DBT_CLOUD_DEV_ROLE;

GRANT USAGE ON WAREHOUSE ANALYST_WH TO ROLE DATA_ANALYST_ROLE;
GRANT USAGE ON WAREHOUSE ANALYST_WH TO ROLE IR_ANALYST_ROLE;
GRANT USAGE ON WAREHOUSE ANALYST_WH TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT USAGE ON WAREHOUSE ANALYST_WH TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT USAGE ON WAREHOUSE ANALYST_WH TO ROLE FINANCIAL_AID_ANALYST_ROLE;

-- ============================================================
-- SECTION 4: DATABASE AND SCHEMA GRANTS
-- ============================================================

-- ───────────────────────────────────────────────────────────
-- Engineering Roles - Full Access to All Layers
-- ───────────────────────────────────────────────────────────

-- DATA_ENGINEER_ROLE
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE DATA_ENGINEER_ROLE;
GRANT USAGE ON ALL SCHEMAS IN DATABASE DITTEAU_DATA TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA DITTEAU_DATA.DEPOSIT TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ENGINEER_ROLE;

-- Future grants for engineers
GRANT SELECT, INSERT, UPDATE, DELETE ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DEPOSIT TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DATA_ENGINEER_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ENGINEER_ROLE;

-- DBT_CLOUD_PROD_ROLE (similar to engineer but for service account)
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT USAGE ON ALL SCHEMAS IN DATABASE DITTEAU_DATA TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DEPOSIT TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON SCHEMA DITTEAU_DATA.DETERGE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON ALL TABLES IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DBT_CLOUD_PROD_ROLE;

-- Future grants for dbt prod
GRANT ALL ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DETERGE TO ROLE DBT_CLOUD_PROD_ROLE;
GRANT ALL ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DBT_CLOUD_PROD_ROLE;

-- ───────────────────────────────────────────────────────────
-- Analyst Roles - Read-Only Access to DISTRIBUTE Layer
-- ───────────────────────────────────────────────────────────

-- Grant to all analyst roles
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE DATA_ANALYST_ROLE;
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE IR_ANALYST_ROLE;
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT USAGE ON DATABASE DITTEAU_DATA TO ROLE FINANCIAL_AID_ANALYST_ROLE;

GRANT USAGE ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ANALYST_ROLE;
GRANT USAGE ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE IR_ANALYST_ROLE;
GRANT USAGE ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT USAGE ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT USAGE ON SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE FINANCIAL_AID_ANALYST_ROLE;

-- Grant SELECT on all tables and views in DISTRIBUTE
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ANALYST_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE IR_ANALYST_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE FINANCIAL_AID_ANALYST_ROLE;

GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ANALYST_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE IR_ANALYST_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE FINANCIAL_AID_ANALYST_ROLE;

-- Future grants for analysts
GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ANALYST_ROLE;
GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE IR_ANALYST_ROLE;
GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT SELECT ON FUTURE TABLES IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE FINANCIAL_AID_ANALYST_ROLE;

GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE DATA_ANALYST_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE IR_ANALYST_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE REGISTRAR_ANALYST_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE ENROLLMENT_ANALYST_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DITTEAU_DATA.DISTRIBUTE TO ROLE FINANCIAL_AID_ANALYST_ROLE;

-- ───────────────────────────────────────────────────────────
-- IR Analysts - Also Need DETERGE Access for Custom Analysis
-- ───────────────────────────────────────────────────────────

GRANT USAGE ON SCHEMA DITTEAU_DATA.DETERGE TO ROLE IR_ANALYST_ROLE;
